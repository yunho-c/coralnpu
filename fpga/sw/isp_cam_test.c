int main() {
  uart_init(CLOCK_FREQUENCY_MHZ);

  uart_puts("ISP Config 324x324\n");
  isp_config_324x324();

  uart_puts("Readback ISP_CTRL\n");
  volatile uint32_t readback = ISP_READ(0x00000400);
  if (readback == 0x00207211) {
    uart_puts("ISP_CTRL OK\n");
    return 0;
  } else {
    uart_puts("ISP_CTRL failed\n");
    return 1;
  }
}
